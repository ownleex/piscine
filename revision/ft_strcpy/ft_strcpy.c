#include <stdio.h>

char	*ft_strcpy(char *dest, char *src)
{
	int	i;
	
	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

int	main(void)
{
	char source[] = "source";
	char destination[255];
	
	printf("la destination est : %s", ft_strcpy(destination, source));
	return (0);
}
