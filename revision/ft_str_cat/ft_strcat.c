#include <stdio.h>

char	*ft_strcat(char *dest, char *src)
{
	int	i;
	int	j;
	
	i = 0;
	j = 0;
	while (dest[i] != '\0')
		i++;
	while (src[j] != '\0')
	{
		dest[i] = src[j];
		i++;
		j++;
	}
	return (dest);
}

int	main(void)
{
	char destination[255] = "test";
	char source[] = "icule";
	printf("%s", ft_strcat(destination, source));
	return (0);
}
